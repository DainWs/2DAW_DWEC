class Product {
    constructor() {}

    setName(name) {
        this.name = name;
    }

    getName() {
        return this.name;
    }

    setPrice(price) {
        this.price = price;
    }

    getPrice() {
        return this.price;
    }

    setCategory(category) {
        this.category = category;
    }

    getCategory() {
        return this.category;
    }
}

export default Product;